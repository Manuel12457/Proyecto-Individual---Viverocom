package com.example.viverocom.Clases;

public class Coordenada {

    private String idpolygon;
    private String latitud;
    private String longitud;
    private String departamentos_iddepartamentos;

    public String getIdpolygon() {
        return idpolygon;
    }

    public void setIdpolygon(String idpolygon) {
        this.idpolygon = idpolygon;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }

    public String getDepartamentos_iddepartamentos() {
        return departamentos_iddepartamentos;
    }

    public void setDepartamentos_iddepartamentos(String departamentos_iddepartamentos) {
        this.departamentos_iddepartamentos = departamentos_iddepartamentos;
    }
}
