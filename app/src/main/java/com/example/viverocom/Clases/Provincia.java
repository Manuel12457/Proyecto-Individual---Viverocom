package com.example.viverocom.Clases;

public class Provincia {

    private String idprovincia;
    private String nombre;
    private String tempmin;
    private String tempmax;
    private String departamentos_iddepartamentos;

    public String getIdprovincia() {
        return idprovincia;
    }

    public void setIdprovincia(String idprovincia) {
        this.idprovincia = idprovincia;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTempmin() {
        return tempmin;
    }

    public void setTempmin(String tempmin) {
        this.tempmin = tempmin;
    }

    public String getTempmax() {
        return tempmax;
    }

    public void setTempmax(String tempmax) {
        this.tempmax = tempmax;
    }

    public String getDepartamentos_iddepartamentos() {
        return departamentos_iddepartamentos;
    }

    public void setDepartamentos_iddepartamentos(String departamentos_iddepartamentos) {
        this.departamentos_iddepartamentos = departamentos_iddepartamentos;
    }
}
